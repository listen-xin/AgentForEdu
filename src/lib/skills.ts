export function getSkillBySlug(slug: string): Skill | undefined {
  return skills.find((s) => s.slug === slug);
}

export function detectSkill(userInput: string): Skill {
  const keywords: Record<string, string[]> = {
    "lesson-planning": ["教案", "备课", "教学设计", "教学方案", "课堂设计", "授课计划", "讲稿"],
    "course-design": ["课程设计", "课程大纲", "课程体系", "培养方案", "课程建设", "教学大纲"],
    "ppt-outline": ["ppt", "课件", "幻灯片", "演示文稿", "讲稿", "PPT"],
    "exam-generation": ["试题", "考试", "试卷", "出题", "考题", "考核", "测验", "测试题"],
    "homework-grading": ["作业批改", "批改", "评分", "评语", "作业反馈", "打分", "rubric", "量规"],
  };

  const input = userInput.toLowerCase();

  // Score each skill by keyword matches
  let bestMatch: Skill = skills[0];
  let bestScore = 0;

  for (const skill of skills) {
    const kws = keywords[skill.slug] || [];
    let score = 0;
    for (const kw of kws) {
      if (input.includes(kw.toLowerCase())) {
        score++;
      }
    }
    // Boost if the skill name appears
    if (input.includes(skill.name.toLowerCase())) {
      score += 2;
    }
    if (score > bestScore) {
      bestScore = score;
      bestMatch = skill;
    }
  }

  return bestMatch;
}
