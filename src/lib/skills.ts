export function detectSkill(userInput: string): Skill {
  const keywords: Record<string, string[]> = {
    "lesson-planning": [
      "教案", "备课", "教学方案", "课堂设计", "讲稿",
      "教学目标", "学情分析", "BOPPPS", "学历案", "公开课",
      "一节课", "单次课", "课时教案", "45分钟", "90分钟",
    ],
    "course-design": [
      "课程设计", "课程大纲", "课程体系", "培养方案", "课程建设",
      "教学大纲", "单元规划", "UbD", "逆向设计", "建构性对齐",
      "整门课", "一门课", "课程方案", "课程规划", "学期",
      "学分", "先修课程", "周历", "教学进度",
    ],
    "ppt-outline": [
      "ppt", "课件", "幻灯片", "演示文稿", "PPT",
      "演示", "汇报", "路演", "答辩", "slides",
    ],
    "exam-generation": [
      "试题", "考试", "试卷", "出题", "考题",
      "考核", "测验", "测试题", "期末", "期中",
      "选择题", "填空题", "判断题", "简答题",
    ],
    "homework-grading": [
      "作业批改", "批改", "评分标准", "评语", "作业反馈",
      "打分", "rubric", "量规", "评量", "评分细则",
    ],
  };

  const input = userInput.toLowerCase();

  // Score each skill by keyword matches
  let bestMatch: Skill = skills[0];
  let bestScore = 0;

  for (const skill of skills) {
    const kws = keywords[skill.slug] || [];
    let score = 0;
    for (const kw of kws) {
      if (input.includes(kw.toLowerCase())) {
        score += 2; // exact keyword match = 2 points
      }
    }
    // Boost if skill name characters appear adjacently
    if (input.includes(skill.name.toLowerCase())) {
      score += 3;
    }
    if (score > bestScore) {
      bestScore = score;
      bestMatch = skill;
    }
  }

  // When no keywords matched (score=0), use heuristics:
  // - "设计" + "课程" without "教案" → course-design
  // - "设计" + "教学" without "课程" → lesson-planning
  // - Contains standalone "PPT" → ppt-outline
  if (bestScore === 0) {
    if (input.includes("设计") && input.includes("课程") && !input.includes("教案")) {
      bestMatch = skills.find((s) => s.slug === "course-design") || bestMatch;
    } else if ((input.includes("设计") || input.includes("写") || input.includes("做")) &&
               (input.includes("教学") || input.includes("课") || input.includes("讲"))) {
      bestMatch = skills.find((s) => s.slug === "lesson-planning") || bestMatch;
    } else if (input.includes("ppt")) {
      bestMatch = skills.find((s) => s.slug === "ppt-outline") || bestMatch;
    }
  }

  return bestMatch;
}
